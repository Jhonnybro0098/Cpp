#include<iostream>
using namespace std;
int main()
{
    int a,b;
    cout<<"Enter the first value : ";
    cin>>a;
    cout<<"Enter the second value : ";
    cin>>b;
    cout<<"Following are the comparison operators in C++"<<endl;
    cout<<"The value of a == b is "<<(a==b)<<endl;
    cout<<"The value of a != b is "<<(a!=b)<<endl;
    cout<<"The value of a >= b is "<<(a>=b)<<endl;
    cout<<"The value of a <= b is "<<(a<=b)<<endl;
    cout<<"The value of a > b is "<<(a>b)<<endl;
    cout<<"The value of a < b is "<<(a<b)<<endl; 
    return 0;
}
//In output '0' is for false and '1' is for true