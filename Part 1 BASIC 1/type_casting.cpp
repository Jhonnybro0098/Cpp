#include <iostream>
using namespace std;
int main()
{
    int a = 6;
    float b = 67.70;
    cout << "The value of a is " << float(a) << endl;
    cout << "The value of b is " << int(b) << endl;
    cout << "The value of a is " << (float)a << endl;
    cout << "The value of b is " << (int)b << endl;
    return 0;
}